    private void mIvestiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mIvestiActionPerformed

       JFileChooser FC = new JFileChooser(".");
       FileNameExtensionFilter filtras = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
       FC.setFileFilter(filtras);
       
       FC.setDialogTitle("Pasirinkite norimą failą");
       FC.setApproveButtonText("Atidaryti failą");
       int rez = FC.showOpenDialog(null); 
       
        if(rez == JFileChooser.APPROVE_OPTION)
        {
            
            File f1 = FC.getSelectedFile();
            knygaBan.loadAndPrint(f1, tekstoLaukas);
            mIvesti.setEnabled(false);
             if(knygaBan.knygos.size()>0)
             {
                 MRikiavimas.setEnabled(true);
                 MAtrinkimas.setEnabled(true);
             }

        }
             else if (rez == JFileChooser.ERROR_OPTION)
                {
			JOptionPane.showMessageDialog(null,
			"Klaida pasirinktame faile",
			"Skaitymas - rašymas", JOptionPane.ERROR_MESSAGE);
		}
              else if (rez == JFileChooser.CANCEL_OPTION)
                {
			JOptionPane.showMessageDialog(null, 
			"Skaitymo atsisakyta (paspaustas ESC arba Cancel)",
			"Skaitymas - rašymas", JOptionPane.WARNING_MESSAGE);
		}

    }//GEN-LAST:event_mIvestiActionPerformed

