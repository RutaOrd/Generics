    private void bVykdytiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bVykdytiActionPerformed
        
        String nuoTekstas = textNuo.getText();
        String ikiTekstas = textIki.getText();
        int nuo = Integer.parseInt(nuoTekstas);
        int iki = Integer.parseInt(ikiTekstas);

        tekstoLaukas.append("\nAtrinkimas pagal kainą:\n");
        atrinkti.visos = knygaBan.knygos;
        knygaBan.knygos = atrinkti.atrinktiPagalPsl(nuo, iki);
        if(knygaBan.knygos.size() > 0)
        {
            tekstoLaukas.append(knygaBan.toString());
        }
        else
        {
            tekstoLaukas.append("Knygų, kurių kaina būtų tokiose ribose, nėra");
        }
         bVykdyti.setEnabled(false);
    }//GEN-LAST:event_bVykdytiActionPerformed

    private void miLenteleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miLenteleActionPerformed
        JFrame fr = new JFrame();
            JPanel panel = new JPanel();
            panel.setLayout(new BorderLayout());
        
            JTable lentele = new JTable();
            String stulpeliai[] = {"Pavadinimas", "Autorius", "Žanras", 
            "Išleidimo metai", "Puslapių sk.", "Kaina" };
            DefaultTableModel lentelesMod = (DefaultTableModel) lentele.getModel();
            JScrollPane valdymas = new JScrollPane(lentele);
            panel.add(new JLabel("Duomenys", SwingConstants.CENTER), BorderLayout.WEST);
            panel.add(valdymas, BorderLayout.CENTER);
            lentelesMod.setColumnIdentifiers(stulpeliai);
            
            for(Knyga k: knygaBan.knygos)
            {
                lentelesMod.addRow(new Object[] {k.ImtiPavadinima(),
                    k.ImtiAutoriu(), k.ImtiZanra(), k.ImtiMetus(), 
                    k.ImtiPuslapius(), k.ImtiKaina()});
 
            }
             fr.add(panel);
             fr.setSize(600, 200);
             fr.setVisible(true);
    }//GEN-LAST:event_miLenteleActionPerformed

